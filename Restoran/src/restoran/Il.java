package restoran;

/**
 *
 * @author Kemal
 */
public class Il {
    private int Id;
    private String name;
    private int country_id;

    public Il(int Id, String name, int country_id) {
        this.Id = Id;
        this.name = name;
        this.country_id = country_id;
    }

    public int getId() {
        return Id;
    }

    public void setId(int Id) {
        this.Id = Id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCountry_id() {
        return country_id;
    }

    public void setCountry_id(int country_id) {
        this.country_id = country_id;
    }
    
}
