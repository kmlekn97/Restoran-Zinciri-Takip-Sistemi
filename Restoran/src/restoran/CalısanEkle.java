    public void kullabicilistele() {
        if (yetki == 5) {
            ArrayList<Kullanici> kullanicilar = RestoranDB.Kullanicilar.KullanicilariListele();
            for (Kullanici item : kullanicilar) {
                model = (DefaultTableModel) jTable1.getModel();
                Object[] row = new Object[3];
                row[0] = item.getKullaniciAdi();
                row[1] = RestoranDB.YetkiToString(String.valueOf(item.getYetki()));
                row[2] = item.getKullaniciId();
                model.addRow(row);
            }
        } else {
            ArrayList<Calisanlar> kullanicilar = RestoranDB.Kullanicilar.KullanicilariListeleRestoran(calisanrestoran_id);
            for (Calisanlar item : kullanicilar) {
                model = (DefaultTableModel) jTable1.getModel();
                Object[] row = new Object[3];
                row[0] = item.getKullaniciAdi();
                row[1] = RestoranDB.YetkiToString(String.valueOf(item.getYetki()));
                row[2] = item.getKullaniciId();
                model.addRow(row);
            }
        }
    }

    public void restoranlar() {
        ArrayList<Restoranlar> restoran = RestoranDB.Restoranlarim.TümRestoranlaricarisizListele();
        for (Restoranlar item : restoran) {
            jComboBox1.addItem(item.getRestoran_adi());
            restoran_id = item.getRestoran_id();
            Restoran_adi = item.getRestoran_adi();
        }
    }

    public void restoranadibul() {
        String ad = jComboBox1.getSelectedItem().toString();
        ArrayList<Restoranlar> restoran = RestoranDB.Restoranlarim.RestoranlariListelead(ad);
        for (Restoranlar item : restoran) {
            res = item.getRestoran_id();
        }
    }

