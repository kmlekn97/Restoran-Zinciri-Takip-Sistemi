    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Object[] noSaveOption = {"Evet", "Hayır"}; // Obje Dizisi Oluşturduk

        String message = "Kullanıcıyı Silmek İstediğinize Emin Misiniz?"; // Verilecek Mesaj

        String title = "Dikkat!!"; // Pencere Başlığı
        int noSave = JOptionPane.showOptionDialog(null, message, title, JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE, null, noSaveOption, null);

        if (noSave == JOptionPane.YES_OPTION) {
            // Kullanıcı Evet'e Bastıysa
            RestoranDB.Malzemeler.Malzeme_Sil(malzeme_ids);
            model.getDataVector().removeAllElements();
            malzemelistele();

        } else if (noSave == JOptionPane.NO_OPTION) {
            // Kullanıcı Hayır'ya Bastıysa
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        int durum = 0;
        ArrayList<Malzeme> malzemeler;
        if (yetki == 5) {
            malzemeler = RestoranDB.Malzemeler.TümMalzemeleriListele();
        } else {
            malzemeler = RestoranDB.Malzemeler.RestoranMalzemeleriListele(calisanrestoran_id);
        }

        for (Malzeme item : malzemeler) {
            if (item.getFatura_durum() == 0) {
                durum = 1;
            }
        }
        if (durum == 1) {
            FATURA_KES fatura = new FATURA_KES(calisankullanici_id);
            fatura.show();
            this.hide();
        } else {
            Object[] noSaveOption = {"TAMAM"}; // Obje Dizisi Oluşturduk

            String message = "Kesilecek Fatura Kalmadı..."; // Verilecek Mesaj

            String title = "Dikkat!!"; // Pencere Başlığı
            int noSave = JOptionPane.showOptionDialog(null, message, title, JOptionPane.CANCEL_OPTION, JOptionPane.ERROR_MESSAGE, null, noSaveOption, null);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

