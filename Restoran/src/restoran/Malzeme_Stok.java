    public Malzeme_Stok(int kullanici_id) {
        initComponents();
        ArrayList<Calisanlar> kullanici = RestoranDB.Kullanicilar.KullanicilariListele(kullanici_id);
        for (Calisanlar item : kullanici) {
            calisanrestoran_id = item.getRestoran_id();
            calisankullanici_id = item.getKullanici_id();
            yetki = item.getYetki();
            jLabel5.setText(item.getCalisan_adi() + " " + item.getCalisan_soyadi());
        }
        ArrayList<Restoranlar> restoran = RestoranDB.Restoranlarim.RestoranlariListele(calisanrestoran_id);
        for (Restoranlar item : restoran) {
            R_adi = item.getRestoran_adi();
        }
        restoranlar();
        malzemeler();
        malzemelistelegenel();
        if (yetki == 4) {
            jComboBox1.setEnabled(false);
            jComboBox1.setSelectedItem(R_adi);
        }
    }

    private int restoran_id, malzeme_id, malzeme_ids;
    String Restoran_adi = "";
    private int res = 0;
    private DefaultTableModel model;

    public void restoranlar() {
        jComboBox1.addItem("Tüm Restoranlar");
        ArrayList<Restoranlar> restoran = RestoranDB.Restoranlarim.TümRestoranlaricarisizListele();
        for (Restoranlar item : restoran) {
            jComboBox1.addItem(item.getRestoran_adi());
            restoran_id = item.getRestoran_id();
            Restoran_adi = item.getRestoran_adi();
        }
    }

    public void restoranadibul() {
        String ad = jComboBox1.getSelectedItem().toString();
        ArrayList<Restoranlar> restoran = RestoranDB.Restoranlarim.RestoranlariListelead(ad);
        for (Restoranlar item : restoran) {
            res = item.getRestoran_id();
        }
    }

    public void malzemeler() {
        ArrayList<Malzeme_Kayit> malzeme = RestoranDB.Malzeme_Kayitlari.MalzemeleriListele();
        for (Malzeme_Kayit item : malzeme) {
            jComboBox2.addItem(item.getMalzeme_adi());
        }
    }

    public void malzemeadibul() {
        String ad = jComboBox2.getSelectedItem().toString();
        ArrayList<Malzeme_Kayit> malzeme = RestoranDB.Malzeme_Kayitlari.MalzemeListelead(ad);
        for (Malzeme_Kayit item : malzeme) {
            malzeme_id = item.getMalzeme_id();
        }
    }

    public void malzemelistele() {
        ArrayList<Malzeme> malzemeler = RestoranDB.Malzemeler.RestoranMalzemeleriListele(res);
        for (Malzeme item : malzemeler) {
            model = (DefaultTableModel) jTable1.getModel();
            Object[] row = new Object[5];
            row[0] = item.getStok_kayit().getMalzeme_adi();
            row[1] = item.getMalzeme_miktar();
            row[2] = currency.format(item.getMalzeme_birim_fiyat());
            row[3] = currency.format(item.getMalzeme_fiyat());
            row[4] = item.getMalzeme_id();
            model.addRow(row);
        }
    }

    public void malzemelistelegenel() {
        try {
            model.getDataVector().removeAllElements();
        } catch (Exception e) {
        }
        ArrayList<Malzeme> malzemeler = RestoranDB.Malzemeler.MalzemeListeleGenel();
        for (Malzeme item : malzemeler) {
            model = (DefaultTableModel) jTable1.getModel();
            Object[] row = new Object[5];
            row[0] = item.getStok_kayit().getMalzeme_adi();
            row[1] = item.getMalzeme_miktar();
            row[2] = currency.format(item.getMalzeme_birim_fiyat());
            row[3] = currency.format(item.getMalzeme_fiyat());
            row[4] = item.getMalzeme_id();
            model.addRow(row);
        }
    }

