package restoran;

/**
 *
 * @author Kemal
 */
public class Ilce {
    private int Id;
    private String name;
    private int state_id;

    public Ilce(int Id, String name, int state_id) {
        this.Id = Id;
        this.name = name;
        this.state_id = state_id;
    }

    public int getId() {
        return Id;
    }

    public void setId(int Id) {
        this.Id = Id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getState_id() {
        return state_id;
    }

    public void setState_id(int state_id) {
        this.state_id = state_id;
    }
    
    
}
